const dicts: Record<Lang, Dict> = { pt, en };

interface I18nCtx {
    lang: Lang;
    setLang: (l: Lang) => void;
    toggleLang: () => void;
    t: (key: string, params?: Record<string, string | number>) => string;
}

export const I18nContext = createContext<I18nCtx>({
    lang: 'pt',
    setLang: () => { },
    toggleLang: () => { },
    t: (k) => k,
});

function interpolate(str: string, params?: Record<string, string | number>) {
    if (!params) return str;
    return str.replace(/\{(\w+)\}/g, (_, k) => String(params[k] ?? ''));
}

export function I18nProvider({ children }: { children: ReactNode }) {
    const [lang, setLangState] = useState<Lang>(() => {
        const saved = localStorage.getItem('lang') as Lang | null;
        if (saved === 'pt' || saved === 'en') return saved;
        return navigator.language.toLowerCase().startsWith('en') ? 'en' : 'pt';
    });

    useEffect(() => {
        localStorage.setItem('lang', lang);
        document.documentElement.setAttribute('lang', lang === 'en' ? 'en' : 'pt-BR');
    }, [lang]);

    const setLang = (l: Lang) => setLangState(l);
    const toggleLang = () => setLangState((l) => (l === 'pt' ? 'en' : 'pt'));
    const t = (key: string, params?: Record<string, string | number>) =>
        interpolate(dicts[lang][key] ?? key, params);

    return (
        <I18nContext.Provider value={{ lang, setLang, toggleLang, t }}>
            {children}
        </I18nContext.Provider>
    );
}
